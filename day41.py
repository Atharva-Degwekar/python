# Short hand if else
# This is a short hand if else statement. It is used to write a single line if else statement. It is used when there is only one statement to be executed in the if or else block. It is written as:
# statement if condition else statement
# It is used to check a condition and execute a statement if the condition is true. If the
# condition is false, the statement after the else keyword is executed.
A = 2
B = 330
print(A) if A > B else print(f"{B} is greater than {A}")
print(B) if A > B else print(f"{A} is lesser than {B}")