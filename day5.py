# Escape Sequences
# Escape sequences are used to insert characters that are difficult to type.

# You can use escape sequences in strings.

# Escape sequences are preceded by a backslash (\).

# Here are some common escape sequences:
# \\ - Backslash
# \' - Single quote
# \" - Double quote
# \n - Newline
# \t - Horizontal tab
# \b - Backspace
# \r - Carriage return
# \f - Form feed
# \ooo - Octal value
# \xhh - Hex value
# The following example demonstrates the use of escape sequences:
print("Hello\nWorld")

# Comments
# Comments are used to explain the code.

# You can use comments to make the code more readable.

# Comments are ignored by the Python interpreter.

# You can use the # symbol to write comments.

# Here is an example of a comment:
# This is a comment.

# Multi-Line Comments
# You can use multi-line comments to write comments that span multiple lines.

# You can use triple quotes (''' or """) to write multi-line comments.

# Here is an example of a multi-line comment:
'''
This is a multi-line comment.
'''

# Print Statements
# You can use the print() function to display text.

# The print() function takes one or more arguments.

# You can use the sep parameter to specify the separator between the arguments.

# You can use the end parameter to specify the end character.

# Here is an example of the print() function:
print("Hello", "World", sep=", ", end="!\n")
print ("HEY", 6 , 7 ,sep="-", end="009|n")  # HEY-6-7009|n this will be the o/p of this line

# so basically sep generates the separator between the arguments and end generates the end character.